    private void btBuscaCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscaCidadeActionPerformed
        String textBusca = textFieldBuscaCidade.getText().toString().trim();
        ConnectionDB con = new ConnectionDB();
        ArrayList<String> ansCon = new ArrayList<String>();
        
        String saida = "Hoteis encontrados em "+textBusca+":\n";
        
        try {
            ansCon = con.buscaPorCidade(textBusca);
            
            for(String ans : ansCon){
                saida += ans + "\n";
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            saida += "Erro. Nenhum hotel nesta cidade!";
        }
        
        if(ansCon.isEmpty()) 
            saida += "Nenhum hotel nesta cidade!";
        labelAnswerCidade.setFont(new Font("Serif", Font.PLAIN, 15));
        labelAnswerCidade.setText(saida);
    }//GEN-LAST:event_btBuscaCidadeActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ConnectionDB con = new ConnectionDB();
        ArrayList<String> ansCon = new ArrayList<String>();
        
        String saida = "Lista de Hoteis:\n";
        
        if(cbMediaServico.isSelected()) {
            try {
                ResultSet ansResult = con.buscaHoteisMediaServico();

                while(ansResult.next()){                           
                    double media = ansResult.getDouble(2);
                    media = (double) Math.round(media*100) / 100;
                    
                    saida += "Média: " + String.valueOf(media) + "\t" + ansResult.getString(1) + "\n";
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                saida += "Erro!";
            }
        } else {
            try {
                ansCon = con.buscaTodosHoteis(cbEstacionamento.isSelected(), cbRestaurante.isSelected(), cbPiscina.isSelected());

                for(String ans : ansCon){
                    saida += ans + "\n";
                }
            } catch (SQLException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                saida += "Erro!";
            }
        }
        
        labelAnswerAll.setFont(new Font("Serif", Font.PLAIN, 15));
        labelAnswerAll.setText(saida);
    }//GEN-LAST:event_jButton1ActionPerformed

