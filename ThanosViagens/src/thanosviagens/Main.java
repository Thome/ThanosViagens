    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        JOptionPane.showMessageDialog(rootPane, "Bem vindo ao sistema Thanos. Encontre aqui o hotel ideal para você!");
        ConnectionDB con = new ConnectionDB();
        String saida = "Nome do Usuário - Comentários - Franquia:\n";
        
        try {
            ResultSet result = con.buscaComentarios();
            int cont = 1;
            while(result.next()){
                String usuario = result.getString(1);
                String comentario = result.getString(2);
                String franquia = result.getString(3);
                
                saida += String.valueOf(cont) + ". " + usuario + ": " + comentario + " sobre " + franquia +"\n"; 
                cont++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            saida += "Erro!";
        }
        
        labelAnswerComentarios.setText(saida);
    }//GEN-LAST:event_formWindowOpened

    
