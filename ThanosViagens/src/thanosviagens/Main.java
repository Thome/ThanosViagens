    private void btPesquisaFranquiaCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaFranquiaCidadeActionPerformed
        String textBusca = textFieldBuscaCidadesFranquia.getText().toString().trim();
        ConnectionDB con = new ConnectionDB();
        ResultSet ansCon;
        
        String saida = "Hoteis encontrados e cidade correspondente :\n";
        
        try {
            ansCon = con.buscaCidadesHotel(textBusca);
            int cont = 0;
            
            while(ansCon.next()){
                saida += ansCon.getString(1) + " em ";
                saida += ansCon.getString(2) + "\n";
                cont++;
            }
            if(cont == 0){
                saida += "Não foram encontradas Franquias desse hotel";
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            saida += "Erro!";
        }
        
        labelAnswerComentarios.setFont(new Font("Serif", Font.PLAIN, 15));
        labelAnswerComentarios.setText(saida);
    }//GEN-LAST:event_btPesquisaFranquiaCidadeActionPerformed

